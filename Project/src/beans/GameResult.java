package beans;

public class GameResult {
	private int gameid;
	private int playerid;
	private int yakusyokuid;
	private int winresult;
	
	public int getGameid() {
		return gameid;
	}
	public void setGameid(int gameid) {
		this.gameid = gameid;
	}
	public int getPlayerid() {
		return playerid;
	}
	public void setPlayerid(int playerid) {
		this.playerid = playerid;
	}
	public int getYakusyokuid() {
		return yakusyokuid;
	}
	public void setYakusyokuid(int yakusyokuid) {
		this.yakusyokuid = yakusyokuid;
	}
	public int getWinresult() {
		return winresult;
	}
	public void setWinresult(int winresult) {
		this.winresult = winresult;
	}
	
	
}
